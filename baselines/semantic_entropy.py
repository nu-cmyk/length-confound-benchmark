"""SemanticEntropy baseline (Kuhn et al. 2023; Farquhar et al. 2024).

For each prompt, the K=10 stochastic samples generated by selfcheck_generate.py
are clustered by bidirectional semantic equivalence using a DeBERTa-large-MNLI
entailment classifier. The number of distinct clusters (or the entropy of the
cluster distribution) is the uncertainty signal: more clusters means the model
is producing semantically diverse outputs, which correlates with hallucination.

This implementation runs the NLI model in fp32. DeBERTa-v1 has broken fp16
support in newer transformers versions.

Restricted to the same 6 closed-book conditions as SelfCheckGPT.
"""
import os
os.environ['TOKENIZERS_PARALLELISM'] = 'false'

import time
from dataclasses import dataclass
from pathlib import Path
from typing import List, Optional
import numpy as np
from tqdm import tqdm
import torch
import torch.nn.functional as F


SELFCHECK_DIR = './data/selfcheck'
CACHE_DIR = './data/cache'
OUT_DIR = './data/semantic_entropy'
os.makedirs(OUT_DIR, exist_ok=True)


@dataclass
class Config:
    nli_model_name: str = 'microsoft/deberta-large-mnli'
    entail_threshold: float = 0.5
    batch_size: int = 32


# ---------------------------------------------------------------------------
# NLI inference
# ---------------------------------------------------------------------------
@torch.no_grad()
def nli_batch(tokenizer, model,
              premises: List[str], hypotheses: List[str],
              device: str = 'cuda', batch_size: int = 32) -> np.ndarray:
    """Batched NLI prediction. Returns (N, 3) probability array."""
    N = len(premises)
    all_probs = np.zeros((N, 3), dtype=np.float32)
    for start in range(0, N, batch_size):
        end = min(start + batch_size, N)
        inputs = tokenizer(
            premises[start:end], hypotheses[start:end],
            return_tensors='pt', truncation=True, max_length=256,
            padding=True).to(device)
        logits = model(**inputs).logits
        all_probs[start:end] = F.softmax(logits, dim=-1).cpu().numpy()
    return all_probs


# ---------------------------------------------------------------------------
# Bidirectional-entailment clustering
# ---------------------------------------------------------------------------
def cluster_samples_bidirectional(tokenizer, model,
                                  samples: List[str],
                                  question: Optional[str] = None,
                                  entail_threshold: float = 0.5,
                                  device: str = 'cuda') -> List[int]:
    """Cluster samples by bidirectional NLI entailment.

    Two samples are in the same cluster iff both (A entails B) and (B entails A)
    according to the DeBERTa-MNLI classifier above the given threshold.
    Returns a cluster id per sample.
    """
    K = len(samples)
    if question:
        samples_with_q = [f"{question} {s}" for s in samples]
    else:
        samples_with_q = samples

    # All ordered pairs (i != j)
    premises, hypotheses, ij_pairs = [], [], []
    for i in range(K):
        for j in range(K):
            if i == j:
                continue
            premises.append(samples_with_q[i])
            hypotheses.append(samples_with_q[j])
            ij_pairs.append((i, j))

    if not premises:
        return list(range(K))

    probs = nli_batch(tokenizer, model, premises, hypotheses, device=device)
    entail_idx = 2  # DeBERTa-large-mnli: 0=contradiction, 1=neutral, 2=entail

    entailment_matrix = np.zeros((K, K), dtype=bool)
    np.fill_diagonal(entailment_matrix, True)
    for idx, (i, j) in enumerate(ij_pairs):
        if probs[idx, entail_idx] > entail_threshold:
            entailment_matrix[i, j] = True
    bidir = entailment_matrix & entailment_matrix.T

    # Union-find over bidirectional-entailment edges
    parent = list(range(K))

    def find(x):
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def union(x, y):
        rx, ry = find(x), find(y)
        if rx != ry:
            parent[rx] = ry

    for i in range(K):
        for j in range(i + 1, K):
            if bidir[i, j]:
                union(i, j)

    clusters = [find(i) for i in range(K)]
    unique = {c: idx for idx, c in enumerate(set(clusters))}
    return [unique[c] for c in clusters]


def semantic_entropy(cluster_ids: List[int]) -> float:
    """Shannon entropy over the empirical cluster distribution."""
    K = len(cluster_ids)
    if K == 0:
        return 0.0
    counts = np.bincount(cluster_ids)
    probs = counts / K
    probs = probs[probs > 0]
    return float(-np.sum(probs * np.log(probs)))


def num_clusters(cluster_ids: List[int]) -> int:
    return len(set(cluster_ids))


# ---------------------------------------------------------------------------
# Driver
# ---------------------------------------------------------------------------
def process_condition(model_name: str, dataset: str,
                      tokenizer, nli_model):
    prefix = model_name.replace('/', '_')
    sc_path = f"{SELFCHECK_DIR}/{prefix}_{dataset}_selfcheck.npz"
    ft_path = f"{CACHE_DIR}/{prefix}_{dataset}_rtraj_features.npz"
    out_path = f"{OUT_DIR}/{prefix}_{dataset}_semantic_entropy.npz"

    if Path(out_path).exists():
        print(f"  SKIP (cached): {out_path}")
        return out_path
    if not Path(sc_path).exists():
        print(f"  NO SELFCHECK: {sc_path}")
        return None

    print(f"\n{'=' * 70}\n{model_name} + {dataset}\n{'=' * 70}")
    sc = np.load(sc_path, allow_pickle=True)
    ft = np.load(ft_path, allow_pickle=True)
    samples = sc['samples']
    labels = sc['labels'].astype(int)
    questions = ft['questions']
    responses = ft['responses']
    N = len(samples)
    print(f"  N={N}, processing...")

    sem_entropy = np.zeros(N, dtype=np.float32)
    n_clusters_arr = np.zeros(N, dtype=np.int32)

    t0 = time.time()
    n_failed = 0
    for i in tqdm(range(N), desc="Semantic clustering"):
        samp = list(samples[i])
        if not samp or len(samp) < 2:
            sem_entropy[i] = 0.0
            n_clusters_arr[i] = 1
            continue

        samp_full = [str(responses[i])] + [str(s) for s in samp]
        samp_full = [s for s in samp_full if s.strip()]
        if len(samp_full) < 2:
            sem_entropy[i] = 0.0
            n_clusters_arr[i] = 1
            continue

        try:
            clusters = cluster_samples_bidirectional(
                tokenizer, nli_model, samp_full,
                question=str(questions[i]),
                entail_threshold=0.5)
            sem_entropy[i] = semantic_entropy(clusters)
            n_clusters_arr[i] = num_clusters(clusters)
        except Exception as e:
            if n_failed < 3:
                print(f"  sample {i} failed: {e}")
            n_failed += 1
            sem_entropy[i] = 0.0
            n_clusters_arr[i] = 1

        if (i + 1) % 100 == 0:
            torch.cuda.empty_cache()

    elapsed = (time.time() - t0) / 60
    print(f"  Done in {elapsed:.1f} min, failed={n_failed}/{N}")

    np.savez(out_path, sem_entropy=sem_entropy,
             n_clusters=n_clusters_arr, labels=labels)
    print(f"  Saved: {out_path}")

    from sklearn.metrics import roc_auc_score
    try:
        a = roc_auc_score(labels, sem_entropy)
        if a < 0.5:
            a = 1 - a
        print(f"    SemanticEntropy: AUROC={a:.4f}")
    except Exception:
        pass
    try:
        a = roc_auc_score(labels, n_clusters_arr)
        if a < 0.5:
            a = 1 - a
        print(f"    NumClusters:     AUROC={a:.4f}")
    except Exception:
        pass

    return out_path


if __name__ == '__main__':
    from transformers import (AutoTokenizer,
                              AutoModelForSequenceClassification)

    cfg = Config()
    print("Loading NLI model in fp32...")
    tok = AutoTokenizer.from_pretrained(cfg.nli_model_name)
    nli = AutoModelForSequenceClassification.from_pretrained(
        cfg.nli_model_name).to('cuda')
    nli.eval()
    print(f"  id2label: {nli.config.id2label}")
    print(f"  dtype:    {next(nli.parameters()).dtype}")

    experiments = [
        ('Qwen/Qwen2.5-7B-Instruct',            'truthfulqa'),
        ('mistralai/Mistral-7B-Instruct-v0.2',  'truthfulqa'),
        ('Qwen/Qwen2.5-7B-Instruct',            'triviaqa'),
        ('mistralai/Mistral-7B-Instruct-v0.2',  'triviaqa'),
        ('meta-llama/Meta-Llama-3-8B-Instruct', 'triviaqa'),
        ('meta-llama/Meta-Llama-3-8B-Instruct', 'nq_open'),
    ]
    for model_name, dataset in experiments:
        try:
            process_condition(model_name, dataset, tok, nli)
        except Exception as e:
            print(f"  FAILED: {e}")
            import traceback
            traceback.print_exc()

    print("\nSEMANTIC ENTROPY COMPLETE")
